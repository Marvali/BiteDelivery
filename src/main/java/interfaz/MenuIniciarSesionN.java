    private void login(){
        loadDataEmpresas();
        loadDataParticulares();
        String correo = txtCorreo.getText();
        String password = txtPassword.getText();
        if(loginEmpresa(correo, password)){
            JOptionPane.showMessageDialog(null, "Login correcto");
            MenuUsuario ventana = new MenuUsuario();
            ventana.setVisible(true);
            dispose();
        }else if(loginParticular(correo, password)){
        initComponents();
            JOptionPane.showMessageDialog(null, "Login correcto");
            MenuUsuario ventana = new MenuUsuario();
            ventana.setVisible(true);
            dispose();
        } else if(correo.equals("admin@javaeat.com") && password.equals("admin")) {
            JOptionPane.showMessageDialog(null, "Login correcto");
            //open new window MenuAdmin
            MenuAdmin ventana = new MenuAdmin();
            ventana.setVisible(true);
            dispose();
        }else
            JOptionPane.showMessageDialog(null, "Login incorrecto");

            
        

}
    
